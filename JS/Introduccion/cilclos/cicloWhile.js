//? Ciclo Do While: Es similar al ciclo while, con la diferencia que ejecuta el bloque de codigo al menos una vez.
let i = 100;
//* La diferencia con el while es que solamente lleva y que ese condiconal se pone despues del codigo a ejecutar
//* Otra pequeña diferencia del do while con los otros condiconales, es que este condicional ejecuta el bloque de código, al menos una vez
do {
    console.log(i);
    i ++;
} while(i <= 130);

